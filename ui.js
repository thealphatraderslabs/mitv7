function startAutoRefresh() {
  stopAutoRefresh();
  const secs = parseInt(document.getElementById('refreshIntervalSel').value) * 1000;
  _autoTimer = setInterval(() => { if (_lastTicker) runAnalysis(); }, secs);
}
function stopAutoRefresh() {
  if (_autoTimer) { clearInterval(_autoTimer); _autoTimer = null; }
}
function setAutoRefresh(on) {
  _autoOn = on;
  const btn = document.getElementById('autoRefreshBtn');
  btn.textContent = _autoOn ? 'ON' : 'OFF';
  btn.className   = _autoOn ? 'on' : '';
  document.getElementById('rail-refresh').style.color = _autoOn ? 'var(--green)' : '';
  _autoOn ? startAutoRefresh() : stopAutoRefresh();
}

